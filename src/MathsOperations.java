public class MathsOperations {
    static void main(String[] args) {
        int a=30,b=10,c,d,e,f;
        c=a+b;
        d=a-b;
        e=a*b;
        f=a/b;
        System.out.println("c is "+c);
        System.out.println("d is "+d);
        System.out.println("e is "+e);
        System.out.println("f is "+f);
    }
}
